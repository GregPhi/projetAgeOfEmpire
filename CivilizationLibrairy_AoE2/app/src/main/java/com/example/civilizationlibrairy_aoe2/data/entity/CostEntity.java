package com.example.civilizationlibrairy_aoe2.data.entity;

/**
 * a cost entity
 */
public class CostEntity {
    private int Wood;
    private int Gold;
    private int Food;
    private int Stone;

    public CostEntity() {
    }

    public int getWood() {
        return Wood;
    }

    public void setWood(int wood) {
        Wood = wood;
    }

    public int getGold() {
        return Gold;
    }

    public void setGold(int gold) {
        Gold = gold;
    }

    public int getFood() {
        return Food;
    }

    public void setFood(int food) {
        Food = food;
    }

    public int getStone() {
        return Stone;
    }

    public void setStone(int stone) {
        Stone = stone;
    }
}
